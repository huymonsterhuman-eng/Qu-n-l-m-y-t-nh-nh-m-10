    /**
     * Gọi SearchProduct.search() để lọc danh sách sản phẩm.
     * Ánh xạ các giá trị ComboBox của form sang field tương ứng trong SearchProduct.
     *
     * @param luaChon giá trị đang chọn trong jComboBoxLuaChon
     * @param content từ khóa tìm kiếm
     * @return danh sách sản phẩm phù hợp
     */
    public ArrayList<MayTinh> searchFn(String luaChon, String content) {
        ArrayList<MayTinh> allProducts = Daomaytinh.getInstance().selectAll();

        // "Đã xóa" = lọc riêng sản phẩm Ngừng kinh doanh (trangThai=0)
        if ("Đã xóa".equals(luaChon)) {
            ArrayList<MayTinh> discontinued = new ArrayList<>();
            for (MayTinh m : allProducts) {
                if (m.getTrangThai() == 0) {
                    discontinued.add(m);
                }
            }
            return SearchProduct.search(discontinued, content, "Tất cả");
        }

        // Lọc theo Trạng thái: so sánh text "Đang kinh doanh" / "Ngừng kinh doanh"
        if ("Trạng thái".equals(luaChon)) {
            ArrayList<MayTinh> filtered = new ArrayList<>();
            for (MayTinh m : allProducts) {
                String trangThaiStr = (m.getTrangThai() == 1) ? "Đang kinh doanh" : "Ngừng kinh doanh";
                if (content.trim().isEmpty() || trangThaiStr.toLowerCase().contains(content.trim().toLowerCase())) {
                    filtered.add(m);
                }
            }
            return filtered;
        }

        // Ánh xạ tên ComboBox sang field cho SearchProduct
        String field;
        switch (luaChon) {
            case "Mã máy":   field = "Mã máy";   break;
            case "Tên máy":  field = "Tên máy";  break;
            case "Loại máy": field = "Loại máy"; break;
            default:         field = "Tất cả";   break;
        }

        return SearchProduct.search(allProducts, content, field);
    }

