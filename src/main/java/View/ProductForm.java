    public void xoaMayTinhSelect() {
        if (tblSanPham.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm cần xoá!");
            return;
        }
        MayTinh remove = getMayTinhSelect();
        if (remove.getSoLuong() > 0) {
            JOptionPane.showMessageDialog(this,
                "Không thể xóa! Sản phẩm còn " + remove.getSoLuong() + " cái trong kho.",
                "Lỗi xóa", JOptionPane.WARNING_MESSAGE);
            loadDataToTable();
            return;
        }
        int luaChon = JOptionPane.showConfirmDialog(this, "Bạn có muốn xoá sản phẩm này?",
                "Xoá sản phẩm", JOptionPane.YES_NO_OPTION);
        if (luaChon == JOptionPane.YES_OPTION) {
            int ketqua = Daomaytinh.getInstance().delete(remove);
            if (ketqua > 0) {
                JOptionPane.showMessageDialog(this, "Xóa sản phẩm thành công!");
            } else {
                JOptionPane.showMessageDialog(this, "Không thể xóa! Sản phẩm đang được dùng trong phiếu nhập/xuất.",
                        "Lỗi xóa", JOptionPane.ERROR_MESSAGE);
            }
        }
        loadDataToTable();
    }

    private void toggleTrangThaiSelect() {
        if (tblSanPham.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm cần đổi trạng thái!");
            return;
        }
        MayTinh product = getMayTinhSelect();
        int trangThaiMoi = (product.getTrangThai() == 1) ? 0 : 1;
        String tenTrangThai = (trangThaiMoi == 1) ? "Đang kinh doanh" : "Ngừng kinh doanh";
        int confirm = JOptionPane.showConfirmDialog(this,
            "Đổi trạng thái sản phẩm thành \"" + tenTrangThai + "\"?",
            "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            product.setTrangThai(trangThaiMoi);
            int ketqua = Daomaytinh.getInstance().update(product);
            if (ketqua > 0) {
                JOptionPane.showMessageDialog(this, "Đổi trạng thái thành công!");
            } else {
                JOptionPane.showMessageDialog(this, "Đổi trạng thái thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
        loadDataToTable();
    }

    /**
     * Lấy đối tượng MayTinh (Laptop hoặc PC) tương ứng với dòng đang chọn trong bảng.
     * Dùng maMay ở cột 0 để truy vấn lại DB (đảm bảo có đủ thông tin chi tiết).
     *
     * @return MayTinh (thực ra là Laptop hoặc PC) của dòng đang chọn
     */
    public MayTinh getMayTinhSelect() {
        int i_row = tblSanPham.getSelectedRow();
        String maMay = tblModel.getValueAt(i_row, 0).toString();
        // selectById() trả về Laptop hoặc PC instance tùy loại máy
        return Daomaytinh.getInstance().selectById(maMay);
    }

    /**
     * Đổ danh sách sản phẩm đã lọc vào bảng.
     * Dùng chung cho cả tìm kiếm và kết quả import Excel.
     *
     * @param result danh sách sản phẩm cần hiển thị
     */
    public void loadDataToTableSearch(ArrayList<MayTinh> result) {
        try {
            tblModel.setRowCount(0);
            for (MayTinh i : result) {
                String loaimay = (i instanceof Laptop) ? "Laptop" : "PC/Case";
                String trangThaiStr = (i.getTrangThai() == 1) ? "Đang kinh doanh" : "Ngừng kinh doanh";
                tblModel.addRow(new Object[]{
                    i.getMaMay(), i.getTenMay(), i.getSoLuong(),
                    formatter.format(i.getGia()) + "đ",
                    i.getTenCpu(), i.getRam(), i.getRom(), loaimay, trangThaiStr
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

