    /**
     * Tải toàn bộ sản phẩm đang hoạt động (trangThai=1) từ DB vào bảng.
     * Chỉ hiển thị sản phẩm chưa bị xóa mềm.
     */
    public void loadDataToTable() {
        try {
            ArrayList<MayTinh> armt = Daomaytinh.getInstance().selectAll();
            tblModel.setRowCount(0);
            for (MayTinh i : armt) {
                String loaimay = (i instanceof Laptop) ? "Laptop" : "PC/Case";
                String trangThaiStr = (i.getTrangThai() == 1) ? "Đang kinh doanh" : "Ngừng kinh doanh";
                tblModel.addRow(new Object[]{
                    i.getMaMay(), i.getTenMay(), i.getSoLuong(),
                    formatter.format(i.getGia()) + "đ",
                    i.getTenCpu(), i.getRam(), i.getRom(), loaimay, trangThaiStr
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

